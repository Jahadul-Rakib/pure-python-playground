# Input Method Take Value As String
x = input("Please Enter Your First Name ")
y = input("Please Enter Your Last Name ")
print(x + " " + y)


# Convert String Number to Integer
x = input("Please Enter Your First Number ")
y = input("Please Enter Your Last Number ")
print(int(x) + int(y))

# Convert String Number to Float
x = float(input("Please Enter Your First Number "))
y = float(input("Please Enter Your Last Number "))
print(x + y)
# Numeric Data Type

# Integer
x = 10
print(type(x))

# Float
y = 10.10
print(type(y))

# Complex
z = 10 + 1j
print(type(z))

# Boolean
print(type(x < y))

# Sequence Data Type

# String
x = "rakib"
print(type(x))

# List
x = [1,2,3,4,5]
print(type(x))

# Set
x = {1,2,3,4,5}
print(type(x))

# Tuple
x = (1,2,3,4,5)
print(type(x))

